#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	printable(char c)
{
	if (c >= 32 && c <= 126)
		return (1);
	else
		return (0);
}

void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (printable(str[i]) == 1)
		{
			write(1, &str[i], 1);
		}
		else
		{
			ft_putchar('\\');
			ft_putchar("0123456789abcdef"[(unsigned char)str[i] / 16]);
			ft_putchar("0123456789abcdef"[(unsigned char)str[i] % 16]);
		}
		i++;
	}
}

int main ()
{
	ft_putstr_non_printable("\n\t\v\f");
}

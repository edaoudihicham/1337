int	ft_sqrt(int nb)
{
	int	sqr;

	sqr = 2;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	while (sqr * sqr < nb)
		sqr++;
	if (sqr * sqr == nb)
		return (sqr);
	return (0);
}

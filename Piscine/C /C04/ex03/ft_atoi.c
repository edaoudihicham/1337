int	ft_atoi(char *s)
{
	int			i;
	int			sign;
	long		r;

	i = 0;
	r = 0;
	sign = 1;
	while (s[i] == 32 || (s[i] >= 9 && s[i] <= 13))
		i++;
	while (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sign *= (-1);
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		r *= 10;
		r += s[i] - '0';
		i++;
	}
	return (sign * (int)r);
}
